def extract_frame(signal, fs, start_time, frame_length_ms=30):
    N = int(frame_length_ms * 1e-3 * fs)
    start = int(start_time * fs)
    frame = signal[start:start+N]

    # Hamming window (manual)
    n = np.arange(len(frame))
    window = 0.54 - 0.46*np.cos(2*np.pi*n/(len(frame)-1))
    return frame * window

############################################################
# AUTOCORRELATION (manual implementation)
############################################################

def autocorrelation(x, order):
    N = len(x)
    r = np.zeros(order+1)

    for k in range(order+1):
        for n in range(N-k):
            r[k] += x[n]*x[n+k]
    return r

############################################################
# LEVINSON-DURBIN (from scratch)
############################################################

def levinson_durbin(r, order):
    a = np.zeros(order+1)
    e = r[0]

    a[0] = 1.0

    for i in range(1, order+1):
        acc = 0.0
        for j in range(1, i):
            acc += a[j]*r[i-j]

        k = -(r[i] + acc)/e

        a_prev = a.copy()
        a[i] = k

        for j in range(1, i):
            a[j] = a_prev[j] + k*a_prev[i-j]

        e *= (1 - k*k)

    return a, e

############################################################
# DTFT (explicit computation)
############################################################

def compute_dtft_psd(x, nfft=1024):
    w = np.linspace(0, np.pi, nfft)
    S = np.zeros(nfft)

    for i, wi in enumerate(w):
        acc_real = 0.0
        acc_imag = 0.0
        for n in range(len(x)):
            acc_real += x[n]*np.cos(-wi*n)
            acc_imag += x[n]*np.sin(-wi*n)
        S[i] = acc_real**2 + acc_imag**2

    return w, S

############################################################
# LP SPECTRUM FROM COEFFICIENTS
############################################################

def lp_spectrum(a, e, nfft=1024):
    w = np.linspace(0, np.pi, nfft)
    A = np.zeros(nfft)

    for i, wi in enumerate(w):
        real = 0.0
        imag = 0.0
        for k in range(len(a)):
            real += a[k]*np.cos(-wi*k)
            imag += a[k]*np.sin(-wi*k)
        A[i] = e / (real**2 + imag**2)

    return w, A

