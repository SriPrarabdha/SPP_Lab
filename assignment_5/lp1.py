def compute_psd(frame, nfft=1024):
    X = np.fft.rfft(frame, nfft)
    Sxx = (np.abs(X)**2) / len(frame)
    return Sxx

def stlp_autocorrelation(Sxx, q, order):
    """
    r_q[k] = IDTFT{ Sxx^(1/q) }
    """
    S_mod = Sxx ** (1.0/q)

    # Recreate symmetric spectrum for IFFT
    full_spec = np.concatenate([S_mod, S_mod[-2:0:-1]])
    r = np.real(np.fft.ifft(full_spec))

    return r[:order+1]

def solve_lp_from_r(r, order):
    R = toeplitz(r[:-1])
    a = np.linalg.solve(R, -r[1:])
    return a

def ar_psd(a, nfft=1024):
    A = np.fft.rfft(np.concatenate([[1], a]), nfft)
    H = 1.0 / (np.abs(A)**2 + 1e-12)
    return H

def stlp_estimate(frame, order, q, nfft=1024):

    Sxx = compute_psd(frame, nfft)

    r_q = stlp_autocorrelation(Sxx, q, order)

    a = solve_lp_from_r(r_q, order)

    S_hat_root = ar_psd(a, nfft)

    # Undo compression
    S_hat = S_hat_root ** q

    return Sxx, S_hat

def is_voiced(frame, zcr_thresh=0.08, energy_thresh=0.001):

    zcr = np.mean(np.abs(np.diff(np.sign(frame)))) / 2
    energy = np.mean(frame**2)

    return (zcr < zcr_thresh) and (energy > energy_thresh)

