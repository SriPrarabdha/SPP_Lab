def spectral_transform_autocorr(frame, q, order, nfft=1024):
    """
    Compute STLP transformed autocorrelation.

    Steps:
    1. Compute DTFT PSD of frame
    2. Apply spectral compression (power q)
    3. Inverse DTFT to obtain autocorrelation
    """

    # ---- Step 1: DTFT PSD ----
    w, S = compute_dtft_psd(frame, nfft=nfft)

    # ---- Step 2: spectral compression ----
    S_q = S ** q

    # ---- Step 3: inverse DTFT to get autocorrelation ----
    r_tilde = np.zeros(order + 1)

    dw = np.pi / (nfft - 1)

    for k in range(order + 1):
        acc = 0.0
        for i in range(nfft):
            acc += S_q[i] * np.cos(w[i] * k)
        r_tilde[k] = acc * dw / np.pi

    # ---- numerical safety ----
    if r_tilde[0] <= 0:
        r_tilde[0] = 1e-6

    return r_tilde

def stlp_analysis(frame, order, q):
    r_tilde = spectral_transform_autocorr(frame, q, order)
    a, e = levinson_durbin(r_tilde, order)
    return a, e

