from numpy.fft import fft

def lpc_spectrum_from_coeffs(lp_coeffs, nfft):
    """
    Convert LPC (AR) coefficients to spectral envelope |H(e^jw)|
    H(z) = 1 / (1 + sum a_k z^-k)
    """
    a = np.concatenate([[1], lp_coeffs])

    # Frequency response of AR filter
    A = fft(a, nfft)
    H = 1.0 / (np.abs(A) + 1e-10)   # magnitude envelope

    return H

def stft_analysis(audio, window, hop_length, nfft):
    frames = []
    phases = []

    for i in range(num_frames):
        start = i * hop_length
        frame = audio[start:start+len(window)] * window

        spectrum = fft(frame, nfft)

        frames.append(np.abs(spectrum))
        phases.append(np.angle(spectrum))

    return np.array(frames), np.array(phases)

def apply_lpc_envelope(audio, order, nfft):
    reconstructed_spec = []
    _, phases = stft_analysis(audio, window, hop_length, nfft)

    for i in range(num_frames):
        start = i * hop_length
        frame = audio[start:start+window_length] * window

        lp_coeffs, _ = compute_lp_coefficients(frame, order)

        # LPC envelope
        H = lpc_spectrum_from_coeffs(lp_coeffs, nfft)

        # Use ORIGINAL PHASE
        complex_spec = H * np.exp(1j * phases[i])
        reconstructed_spec.append(complex_spec)

    return np.array(reconstructed_spec)

def istft_synthesis(spec_frames, window, hop_length):
    nfft = spec_frames.shape[1]
    output_len = (num_frames-1)*hop_length + window_length
    output = np.zeros(output_len)

    for i, spectrum in enumerate(spec_frames):
        frame = np.real(np.fft.ifft(spectrum))[:window_length]
        start = i * hop_length
        output[start:start+window_length] += frame * window

    # Normalize overlap
    output /= np.max(np.abs(output) + 1e-10)
    return output

def synthesize_from_noise(order):
    synthesized = np.zeros((num_frames-1)*hop_length + window_length)

    for i in range(num_frames):
        # White noise excitation
        excitation = np.random.randn(window_length)

        start = i * hop_length
        frame = audio[start:start+window_length] * window

        lp_coeffs, gain = compute_lp_coefficients(frame, order)

        # LPC synthesis filter: 1 / A(z)
        a = np.concatenate([[1], lp_coeffs])

        synth_frame = lfilter([1], a, excitation)

        synthesized[start:start+window_length] += synth_frame * window

    synthesized /= np.max(np.abs(synthesized)+1e-10)
    return synthesized

