def load_audio(file_loc):
    sample_rate, audio = wav.read(file_loc)
    audio = audio.astype(np.float64)
    audio = audio / np.max(np.abs(audio))
    print(f"Sample Rate: {sample_rate} Hz | Length: {len(audio)/sample_rate:.3f}s")
    return sample_rate, audio

def get_stft_params(sample_rate, window_duration=0.025, hop_duration=0.010):
    window_length = int(window_duration * sample_rate)
    hop_length    = int(hop_duration   * sample_rate)
    window        = hamming(window_length)
    return window_length, hop_length, window

# ─────────────────────────────────────────────────────────────
# STFT (from scratch)
# ─────────────────────────────────────────────────────────────

def compute_stft(audio, window, hop_length):
    """Returns complex STFT: shape (num_frames, nfft//2+1)."""
    wl = len(window)
    nfft = wl
    num_frames = (len(audio) - wl) // hop_length + 1
    stft = np.zeros((num_frames, nfft // 2 + 1), dtype=complex)
    for i in range(num_frames):
        frame = audio[i*hop_length : i*hop_length + wl] * window
        stft[i] = np.fft.rfft(frame, n=nfft)
    return stft

# ─────────────────────────────────────────────────────────────
# PSD: S_xx(e^jw) — magnitude squared of STFT
# ─────────────────────────────────────────────────────────────

def compute_psd(stft):
    """Per-frame power spectral density |X(e^jw)|^2."""
    return np.abs(stft) ** 2

# ─────────────────────────────────────────────────────────────
# STLP: Spectral Transform Linear Prediction
#
#  Step 1: Compute S_xx(e^jw)^(1/q)  — power-compress the PSD
#  Step 2: Inverse DFT → autocorrelation-like sequence r_q[n]
#  Step 3: Solve Yule-Walker (Toeplitz) for LP coefficients
#  Step 4: AR model gives Ŝ_xx(e^jw)  (LP envelope of compressed PSD)
#  Step 5: Raise to power q → Ŝ_xx(e^jw)^q ≈ S_xx(e^jw)
# ─────────────────────────────────────────────────────────────

def compute_lp_coefficients_from_autocorr(r, order):
    """Solve Yule-Walker equations via Toeplitz system."""
    r_norm = r / (r[0] + 1e-10)
    R = toeplitz(r_norm[:order])
    try:
        a = np.linalg.solve(R, -r_norm[1:order+1])
    except np.linalg.LinAlgError:
        a = np.zeros(order)
    return a

def ar_spectrum(lp_coeffs, gain, nfft):
    """
    AR (all-pole) model spectrum magnitude squared:
    Ŝ(e^jw) = gain^2 / |A(e^jw)|^2
    A(e^jw) = 1 + a1*e^{-jw} + ... + ap*e^{-jpw}
    """
    a_full = np.concatenate([[1], lp_coeffs])
    # Zero-pad to nfft for frequency resolution
    A_freq = np.fft.rfft(a_full, n=nfft)
    psd_ar = (gain ** 2) / (np.abs(A_freq) ** 2 + 1e-30)
    return psd_ar

def stlp_frame(frame_psd, order, q, nfft):
    """
    STLP for a single frame.

    Parameters
    ----------
    frame_psd : np.ndarray  — S_xx(e^jw), shape (nfft//2+1,)
    order     : int         — LP order
    q         : int         — compression exponent
    nfft      : int

    Returns
    -------
    S_hat_q   : np.ndarray  — Ŝ_xx(e^jw)^q, the final AR PSD approximation
    S_hat_1q  : np.ndarray  — Ŝ_xx(e^jw), AR model of S^(1/q)
    """
    # Step 1: Power-compress the PSD → S_xx^(1/q)
    S_compressed = frame_psd ** (1.0 / q)           # shape: (nfft//2+1,)

    # Step 2: Reconstruct full two-sided spectrum (symmetric) and IDFT
    # Build real, symmetric full DFT from one-sided
    if nfft % 2 == 0:
        S_full = np.concatenate([S_compressed, S_compressed[-2:0:-1]])
    else:
        S_full = np.concatenate([S_compressed, S_compressed[-1:0:-1]])
    # IDFT → autocorrelation sequence r_q[n]
    r_q = np.real(np.fft.ifft(S_full))              # shape: (nfft,)

    # Step 3: Solve Yule-Walker with r_q[0:order+1]
    r_lp = r_q[:order+1]
    a    = compute_lp_coefficients_from_autocorr(r_lp, order)

    # Step 4: Gain^2 = r_q[0] + sum(a * r_q[1:p+1])
    gain_sq = np.real(r_lp[0] + np.dot(a, r_lp[1:order+1]))
    gain_sq = max(gain_sq, 1e-10)

    # Step 5a: AR model of compressed PSD → Ŝ(e^jw)  (approximates S^(1/q))
    S_hat_1q = ar_spectrum(a, np.sqrt(gain_sq), nfft)   # one-sided

    # Step 5b: Raise back to power q → approximates original PSD
    S_hat_q  = S_hat_1q ** q

    return S_hat_q, S_hat_1q

def run_stlp(audio, sample_rate, order=12, q_values=(1, 2, 3)):
    """
    Run STLP on all frames for each q value.

    Returns
    -------
    results : dict  keyed by q, each value is dict with:
              'S_hat_q'  : (num_frames, nfft//2+1) — approximation of PSD
              'S_hat_1q' : (num_frames, nfft//2+1) — AR model of S^(1/q)
    psd_orig : (num_frames, nfft//2+1) — original PSD S_xx
    freqs    : frequency axis (Hz)
    frames   : list of frame indices
    """
    window_length, hop_length, window = get_stft_params(sample_rate)
    nfft = window_length

    stft     = compute_stft(audio, window, hop_length)
    psd_orig = compute_psd(stft)                         # S_xx(e^jw)
    num_frames = psd_orig.shape[0]
    freqs    = np.fft.rfftfreq(nfft, d=1/sample_rate)

    results = {}
    for q in q_values:
        S_hat_q_all  = np.zeros_like(psd_orig)
        S_hat_1q_all = np.zeros_like(psd_orig)
        for i in range(num_frames):
            S_hat_q_all[i], S_hat_1q_all[i] = stlp_frame(psd_orig[i], order, q, nfft)
        results[q] = {'S_hat_q': S_hat_q_all, 'S_hat_1q': S_hat_1q_all}

    return results, psd_orig, freqs, num_frames, hop_length, nfft

# ─────────────────────────────────────────────────────────────
# VOICED / UNVOICED DETECTION (simple energy + ZCR heuristic)
# ─────────────────────────────────────────────────────────────

def detect_voiced_unvoiced(audio, sample_rate, num_frames, hop_length, window_length):
    """
    Returns array of booleans: True = voiced, for each frame.
    Uses short-time energy + zero crossing rate.
    """
    wl = window_length
    energies, zcrs = [], []
    for i in range(num_frames):
        s = i * hop_length
        frame = audio[s:s+wl]
        energies.append(np.sum(frame**2) / wl)
        zcrs.append(np.sum(np.abs(np.diff(np.sign(frame)))) / (2 * wl))
    energies = np.array(energies)
    zcrs     = np.array(zcrs)

    # Voiced: high energy AND low ZCR
    energy_thresh = np.percentile(energies, 40)
    zcr_thresh    = np.percentile(zcrs, 60)
    voiced = (energies > energy_thresh) & (zcrs < zcr_thresh)
    return voiced, energies, zcrs

# ─────────────────────────────────────────────────────────────
# LOG SPECTRAL DISTANCE per frame
# ─────────────────────────────────────────────────────────────

def log_spectral_distance(psd1, psd2):
    """Per-frame LSD (dB) between two PSD arrays."""
    return np.sqrt(np.mean(
        (10*np.log10(psd1+1e-10) - 10*np.log10(psd2+1e-10))**2,
        axis=1
    ))

# ─────────────────────────────────────────────────────────────
# PICK BEST VOICED & UNVOICED FRAME
# ─────────────────────────────────────────────────────────────

def pick_frames(voiced_mask, energies, num_frames):
    """Return index of best voiced and best unvoiced frame."""
    voiced_idx   = np.where(voiced_mask)[0]
    unvoiced_idx = np.where(~voiced_mask)[0]

    if len(voiced_idx) == 0:
        voiced_idx = [num_frames // 3]
    if len(unvoiced_idx) == 0:
        unvoiced_idx = [num_frames * 2 // 3]

    v_frame  = voiced_idx[np.argmax(energies[voiced_idx])]
    uv_frame = unvoiced_idx[np.argmax(energies[unvoiced_idx])]
    return int(v_frame), int(uv_frame)

